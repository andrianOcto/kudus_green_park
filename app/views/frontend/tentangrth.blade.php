            <li>Manfaat tidak langsung (berjangka panjang dan bersifat intangible), yaitu pembersih udara yang sangat efektif, pemeliharaan akan kelangsungan persediaan air tanah, pelestarian fungsi lingkungan beserta segala isi flora dan fauna yang ada (konservasi hayati atau keanekaragaman hayati)</li>
