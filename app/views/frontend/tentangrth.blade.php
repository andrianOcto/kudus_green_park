        <p style="font-size:1.5em">Menurut Peraturan Menteri Pekerjaan Umum Nomor 5/PRT/M/2008, Ruang Terbuka Hijau (RTH) adalah adalah area memanjang/jalur dan atau mengelompok, yang penggunaannya lebih bersifat terbuka, tempat tumbuh tanaman, baik yang tumbuh tanaman secara alamiah maupun yang sengaja ditanam.</p>
        <h2>Kedudukan Pedoman Penyediaan dan Pemanfaatan RTH dalam Rencana Tata Ruang Wilayah</h2>
        <p>Penataan ruang merupakan suatu sistem proses perencanaan tata ruang, pemanfaatan ruang, dan pengendalian pemanfaatan ruang. Perencanaan tata ruang dilakukan untuk menghasilkan rencana umum tata ruang dan rencana rinci tata ruang.</p>
        <p>Berdasarkan wilayah administrasinya, penataan ruang terdiri atas penataan ruang wilayah nasional, penataan ruang wilayah provinsi, penataan ruang wilayah kabupaten/kota.</p>
        <p>Di dalam Undang-Undang No. 26 Tahun 2007 tentang Penataan Ruang, perencanaan tata ruang wilayah kota harus memuat rencana penyediaan dan pemanfaatan ruang terbuka hijau yang luas minimalnya sebesar 30% dari luas wilayah kota.</p>
        <p>Rencana penyediaan dan pemanfaatan ruang terbuka hijau selain dimuat dalam RTRW Kota, RDTR Kota, atau RTR Kawasan Strategis Kota, juga dimuat dalam RTR Kawasan Perkotaan yang merupakan rencana rinci tata ruang wilayah Kabupaten.</p>
        <h2>Tujuan penyelenggaraan RTH</h2>
        <p>
            <ol>
                <li>Menjaga ketersediaan lahan sebagai kawasan resapan air</li>
                <li>Menciptakan aspek planologis perkotaan melalui keseimbangan antara lingkungan
alam dan lingkungan binaan yang berguna untuk kepentingan masyarakat</li>
                <li>Meningkatkan keserasian lingkungan perkotaan sebagai sarana pengaman
lingkungan perkotaan yang aman, nyaman, segar, indah, dan bersih.</li>
            </ol>
        </p>
        <h2>Fungsi RTH</h2>
        <h3>Fungsi utama (intrinsik) yaitu fungsi ekologis:</h3>
        <ol>
            <li>memberi jaminan pengadaan RTH menjadi bagian dari sistem sirkulasi udara (paru-paru kota)</li>
            <li>pengatur iklim mikro agar sistem sirkulasi udara dan air secara alami dapat berlangsung lancar</li>
            <li>sebagai peneduh</li>
            <li>produsen oksigen</li>
            <li>penyerap air hujan</li>
            <li>penyedia habitat satwa</li>
            <li>penyerap polutan media udara, air dan tanah, serta</li>
            <li>penahan angin</li>
        </ol>
        <h3>Fungsi tambahan (ekstrinsik) yaitu:</h3>
        <ol>
            <li>Fungsi sosial dan budaya:
                <ul>
                    <li>menggambarkan ekspresi budaya lokal</li>
                    <li>merupakan media komunikasi warga kota</li>
                    <li>tempat rekreasi</li>
                    <li>wadah dan objek pendidikan, penelitian, dan pelatihan dalam mempelajari
alam</li>
                </ul>
            </li>
            <li>Fungsi ekonomi
                <ul>
                    <li>sumber produk yang bisa dijual, seperti tanaman bunga, buah, daun, sayur
mayur</li>
                    <li>bisa menjadi bagian dari usaha pertanian, perkebunan, kehutanan dan lain-
lain</li>
                </ul>
            </li>
            <li>Fungsi estetika
                <ul>
                    <li>meningkatkan kenyamanan, memperindah lingkungan kota baik dari skala
mikro: halaman rumah, lingkungan permukimam, maupun makro: lansekap
kota secara keseluruhan</li>
                    <li>menstimulasi kreativitas dan produktivitas warga kota</li>
                    <li>pembentuk faktor keindahan arsitektural</li>
                    <li>menciptakan suasana serasi dan seimbang antara area terbangun dan tidak
terbangun</li>
                </ul>
            </li>
        </ol>
        <p>Dalam suatu wilayah perkotaan, empat fungsi utama ini dapat dikombinasikan sesuai dengan kebutuhan, kepentingan, dan keberlanjutan kota seperti perlindungan tata air, keseimbangan ekologi dan konservasi hayati.</p>
        
        <h2>Manfaat RTH</h2>
        <ol>
